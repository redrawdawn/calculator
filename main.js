const add = (num1, num2) => {
    const sum = num1 + num2
    return sum
}

const subtract = (num1, num2) => {
    const subtract = num1 - num2
    return subtract
}

const divide = (num1, num2) => {
    const divide = num1 / num2
    return divide
}

const square = (num1, num2) => {
    const square = num1 * num2
    return square
}

result = add(20, 25)
console.log(result)

result = subtract(20, 15)
console.log(result)

result = divide(20, 4)
